def str_func(value):
    """

    :param value: слово или строку
    :return: слово или строку со всеми заглавными буквами
    """
    return value.upper()


def str_func_two(value):
    """

    :param value: слово или строку
    :return: слово или строку начинающихся с заглавных букв
    """
    return value.title()
